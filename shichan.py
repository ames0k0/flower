def app(simp, flag):
    s = []
    result = ""
    start = 11
    end = 30
    size = 0
    for each in simp.split('\n')[1:]:
        if each:
            state = randint(start, end)
            if flag:
                s.append(state)
            nline = f"{' ' * (30 - state)}{'.' * state}{each}\n"
            if len(nline) > size:
                size = len(nline)
            result += nline
        else:
            result += each

    print(size)
    return ("\n".join((simp.split('\n', maxsplit=1)[0], result)), s)


